TEST(SHA3256DigestTest,test)
{
    SHA3256Digest sha256_ctx;
    const char *bad_msg = "HelloWorld";

    constexpr size_t kMsgSizeMax = 1024;
    for (size_t msg_size = 0; msg_size <= kMsgSizeMax; ++msg_size) {
        const ExtendedStdString msg(msg_size,'x');
        ASSERT_EQ(msg_size,msg.size());

        const ExtendedStdString expect_digest = HexDecode<ExtendedStdString>(g_x_digest[msg_size]);
        ASSERT_EQ(SHA3256Digest::kDigestSize,expect_digest.size());

        for (size_t update_times = 1; update_times <= msg_size; ++update_times) {

            const size_t update_size1 = msg_size / update_times;
            const size_t update_size2 = msg_size % update_times + update_size1;

            ASSERT_EQ(msg_size,update_size1 * (update_times -1 ) + update_size2);

            // 测试 Final(optr,osize) 当 osize 不足时
            for (size_t osize = 0; osize <= SHA3256Digest::kDigestSize; ++osize) {
                unsigned char digest_buffer[SHA3256Digest::kDigestSize * 2];
                static_assert(sizeof(unsigned char) == 1,"sizeof(unsigned char) == 1 Failed!");

                constexpr int kProguardByteVal = 0x33;
                unsigned char *proguard_begin = &digest_buffer[osize];
                size_t proguard_size = sizeof(digest_buffer) - osize;
                memset(proguard_begin,kProguardByteVal,proguard_size);

                const char *iptr = msg.const_raw_data();

                for (size_t iii = 0; iii < update_times - 1; ++iii) {
                    sha256_ctx.Update(iptr,update_size1);
                    iptr += update_size1;
                }

                sha256_ctx.Update(iptr,update_size2);
                sha256_ctx.Final(digest_buffer,osize);
                EXPECT_EQ(0,memcmp(digest_buffer,expect_digest.const_raw_data(),osize));

                for (size_t idx = 0; idx < proguard_size; ++idx)
                    EXPECT_EQ(kProguardByteVal,proguard_begin[idx]);
            }


            // Reset() 测试.
            {
                for (size_t i = 0; i < update_times; ++i)
                    sha256_ctx.Update(bad_msg,strlen(bad_msg));

                sha256_ctx.Reset();
            }
        }
    }
}


